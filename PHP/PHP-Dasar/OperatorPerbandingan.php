<?php
echo "sama dengan :";
var_dump("10" == 10); //sama dengan
echo "\n";

echo "identik : ";
var_dump("10" === 10); //identik (true jika memiliki tipe data yang sama)
echo "\n";

echo "tidak sama dengan : ";
var_dump("10" != 10); //tidak sama dengan
echo "\n";

echo "tidak identik : ";
var_dump("10" !== 10); //tidak identik (true jika tidak memiliki tipe data yang sama)
echo "\n";

echo "kurang dari : ";
var_dump(9 < 10); //kurang dari
echo "\n";

echo "kurang dari sama dengan : ";
var_dump(10 <= 9); //kurang dari sama dengan
echo "\n";

echo "lebih dari : ";
var_dump(11 > 10); //lebih dari
echo "\n";

echo "lebih dari sama dengan : ";
var_dump(11 >= 10); //lebih dari sama dengan
echo "\n";